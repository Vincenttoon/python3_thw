# Escape Sequences
# What was that?

# \ escapes a single or a double quotation, example
print("I am 5'9\" tall")
print('I am 5\'9" tall\n')

# \t tabs - creates line indentation

tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)

